
public class BFSnode {
	public String letter;
	public int depth;
	
	public BFSnode(){
		letter = " ";
		depth = 0;
	}

	public String getLetter() {
		return letter;
	}

	public void setLetter(String letter) {
		this.letter = letter;
	}

	public int getDepth() {
		return depth;
	}

	public void setDepth(int depth) {
		this.depth = depth;
	}
	
}
